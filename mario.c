#include <cs50.h>
#include <stdio.h>

int main(void)
{
    while (true){
        
        int i = get_int("Enter a number: ");
        if (8>=i && i>=1){
            
            for (int a=0; a<i; a++){
                for (int c=i-a; c>1; c--){
                printf(" ");
                }
                for (int b=0; b<a+1; b++){
                    printf("#");
                }
                printf("\n");                 
            }
        break;
        }      
    }  
}
